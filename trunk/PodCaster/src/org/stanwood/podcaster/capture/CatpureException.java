package org.stanwood.podcaster.capture;

import org.stanwood.podcaster.logging.StanwoodException;

public class CatpureException extends StanwoodException {

	public CatpureException() {
		super();
	}

	public CatpureException(String message, Throwable cause) {
		super(message, cause);
	}

	public CatpureException(String message) {
		super(message);
	}

	public CatpureException(Throwable cause) {
		super(cause);
	}

}
