package org.stanwood.podcaster.cliutils;

public class CatpureException extends Exception {

	public CatpureException() {
		super();
	}

	public CatpureException(String message, Throwable cause) {
		super(message, cause);
	}

	public CatpureException(String message) {
		super(message);
	}

	public CatpureException(Throwable cause) {
		super(cause);
	}

}
